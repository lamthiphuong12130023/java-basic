package Objects;

import javax.swing.*;
import java.awt.*;

public class FormFill {

    String Facility;
    String checkbox;
    String Radio_button;
    String HealthcareProgram;
    String VisitDate;
    String Comment;

    public String getFacility() {
        return Facility;
    }

    public void setFacility(String facility) {
        Facility = facility;
    }

    public String getCheckbox() {
        return checkbox;
    }

    public void setCheckbox(String checkbox) {
        this.checkbox = checkbox;
    }

    public String getRadio_button() {
        return Radio_button;
    }

    public void setRadio_button(String radio_button) {
        Radio_button = radio_button;
    }

    public String getHealthcareProgram() {
        return HealthcareProgram;
    }

    public void setHealthcareProgram(String healthcareProgram) {
        HealthcareProgram = healthcareProgram;
    }

    public String getVisitDate() {
        return VisitDate;
    }

    public void setVisitDate(String visitDate) {
        VisitDate = visitDate;
    }

    public String getComment() {
        return Comment;
    }

    public void setComment(String comment) {
        Comment = comment;
    }




}
